using System;
using System.Collections.Generic;
using System.Text;

namespace TicTocToeGame
{
    class TicTocToeGame
    {
        public void gameBoard()
        {
            char[] board = new char[10];

            {
                for (int i = 1; i < 10; i++)
                {
                    board[i] = ' ';
                }
            }
        }
    }
}
